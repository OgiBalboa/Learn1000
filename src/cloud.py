import sys

# [START storage_upload_file]
from google.cloud import storage


def upload_blob(bucket_name, source_file_name, destination_blob_name):
    """Uploads a file to the bucket."""
    # bucket_name = "your-bucket-name"
    # source_file_name = "local/path/to/file"
    # destination_blob_name = "storage-object-name"

    storage_client = storage.Client()
    bucket = storage_client.bucket(bucket_name)
    blob = bucket.blob(destination_blob_name)

    blob.upload_from_filename(source_file_name)

    print(
        "File {} uploaded to {}.".format(
            source_file_name, destination_blob_name
        )
    )


# [END storage_upload_file]

if __name__ == "__main__":
    try:
        if sys.argv[1] == "upload":
            upload_blob(
                bucket_name=sys.argv[2],
                source_file_name=sys.argv[3],
                destination_blob_name=sys.argv[4],
            )
        elif sys.argv[1] == "download":
                download_blob(
                    bucket_name=sys.argv[2],
                    source_blob_name=sys.argv[3],
                    destination_file_name=sys.argv[4],
                )
        else : raise
    except:
        print("Please insert a valid input , cloud.py upload/download bucket-name path-to-file storage-object-name")
        
